function displayNewQuote() {
    // सूची में से एक यादृच्छिक (random) विचार चुनें
    const randomIndex = Math.floor(Math.random() * hindiQuotes.length);
    const randomQuote = hindiQuotes[randomIndex];

    quoteTextElement.textContent = `"${randomQuote.text}"`;
    quoteAuthorElement.textContent = `- ${randomQuote.author}`;
}

